function add(a, b) { // performs addition on number inputs
    return a + b;
}

function subtract(a, b) { // performs subtraction on number inputs
    return a - b;
}

function multiply(a, b) { // performs multiplication on number inputs
    return a * b;
}

function divide(a, b) { // performs division on number inputs
    return a / b;
}
  

export {
    add,
    subtract,
    multiply,
    divide
}